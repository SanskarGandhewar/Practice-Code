package conditionalControlStatement;

public class Vowel
{
	public static void main(String[] args) 
	{
		char ch='e';
		
		if((ch=='a')||(ch=='e')||(ch=='i')||(ch=='o')||(ch=='u'))
		{
			System.out.println("The Given character is Vowel");
		}
		else
		{
			System.out.println("The Given character is Consonant");
		}
	}
}
